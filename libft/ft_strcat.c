#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		counter;

	counter = ft_strlen(s1);
	while (*s2)
		s1[counter++] = *s2++;
	s1[counter] = '\0';
	return (s1);
}
